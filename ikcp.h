struct IKCPSEG
{
	char *data;
};


struct IKCPFEC
{
	IUINT8 fty;
	IUINT8 data_ptrs_count;
	char **data_ptrs;
};


//---------------------------------------------------------------------
// IKCPCB
//---------------------------------------------------------------------
struct IKCPCB
{
	IUINT16 cid, recv_pid, mtu, nsnd;
	void *user;
	int (*output)(const char *buf, int len, struct IKCPCB *kcp, void *user);
	struct IKCPSEG *segs;
	struct IKCPFEC *fecs;

	IUINT16 input_cid;
	bool should_reset;
	bool session_established;
	bool session_just_established;
	bool session_data_received;

	IUINT16 fid, gid, input_pid;
	IUINT16 recv_fid, input_fid;

	IUINT16 input_fid_count, recv_pid_count, input_pid_count;
};


typedef struct IKCPCB ikcpcb;

